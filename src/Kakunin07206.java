public class Kakunin07206 {
    public static void main(String[] args) {
        int[][] a = { { 1, 2, 3 }, { 4 }, { 5, 6 } };
        System.out.println("a.length = " + a.length);
        System.out.println("a[1].length = " + a[1].length);
        System.out.println("a[2].length = " + a[2].length);
    }
    // a.length = 3
    // a[1].length = 1
    // a[2].length = 2
}
