public class SampleArray02b {
    public static void main(String[] args) {
        // 基本データ型
        byte[] arrayByte = new byte[3];
        short[] arrayShort = new short[3];
        int[] arrayInt = new int[3];
        long[] arrayLong = new long[3];
        float[] arrayFloat = new float[3];
        double[] arrayDouble = new double[3];
        char[] arrayChar = new char[3];
        boolean[] arrayBoolean = new boolean[3];
        String[] arrayString = new String[3];
        System.out.println("byte型配列   :" + arrayByte[0]);
        System.out.println("short型配列   :" + arrayShort[0]);
        System.out.println("int型配列   :" + arrayInt[0]);
        System.out.println("long型配列   :" + arrayLong[0]);
        System.out.println("float型配列   :" + arrayFloat[0]);
        System.out.println("double型配列   :" + arrayDouble[0]);
        System.out.println("char型配列   :" + arrayChar[0]); // 何も出力されない
        System.out.println("boolean型配列   :" + arrayBoolean[0]);
        System.out.println("String型配列   :" + arrayString[0]);
    }
}
