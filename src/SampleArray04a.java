public class SampleArray04a {
    public static void main(String[] args) {
        int[] ai;
        ai = new int[4];
        ai[0] = 78;
        ai[1] = 92;
        ai[2] = 80;
        ai[3] = 100;
        for (int i = 0; i < 4; i++) {
            System.out.println("ai[" + i + "] = " + ai[i]);
        }
    }
}
