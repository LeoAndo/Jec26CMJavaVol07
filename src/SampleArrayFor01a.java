public class SampleArrayFor01a {
    public static void main(String[] args) {
        // 配列の宣言・生成
        int[] score = { 89, 56, 24 };

        // 配列要素の表示(通常の for 文を利用)
        for (int i = 0; i < score.length; i++) {
            System.out.println(score[i]);
        }
    }
    // 89
    // 56
    // 24
}
